def get_user_agent_for_user(telegram_user_id: int) -> str:
    """
    Get a deterministic user agent for a user based on their Telegram ID.

    The same telegram_user_id will always return the same user agent.

    Args:
        telegram_user_id: Telegram user ID

    Returns:
        str: A user agent string from the pool
    """
    index = telegram_user_id % len(USER_AGENTS)
    return USER_AGENTS[index]


def get_random_user_agent() -> str:
    """
    Get a random user agent from the pool.

    Used for web endpoint where no user context is available.

    Returns:
        str: A random modern desktop browser user agent
    """
    return random.choice(USER_AGENTS)
